import React, {useState, useEffect, useRef} from 'react';
import {
    View,
    Text,
    TouchableOpacity,
    ActivityIndicator,
    Platform,
    StyleSheet,
    Dimensions,
    PixelRatio,
    Keyboard,
    Animated,
    ScrollView,
    Alert
} from 'react-native';
import {useDispatch, useSelector} from 'react-redux';
import {login, clearError, setTokens, setUser, loadUserProfile} from '@entities/auth';
import {selectEmail, selectPassword, setEmail, setPassword} from '@entities/auth';
import {CustomTextInput} from '@shared/ui/CustomTextInput/CustomTextInput';
import {clearProfile, fetchProfile} from '@entities/profile';
import {normalize, normalizeFont} from "@shared/lib/normalize";

// –ò–º–ø–æ—Ä—Ç –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ FCM
if (__DEV__) {
    import('../../../../../../debug-fcm-logs').catch(e => console.warn('Debug FCM logs not available:', e));
}

export const LoginForm = () => {
    const dispatch = useDispatch();
    const email = useSelector(selectEmail) || '';
    const password = useSelector(selectPassword) || '';
    const isLoading = useSelector((state) => state.auth?.isLoading ?? false);
    const error = useSelector((state) => state.auth?.error);

    const [emailError, setEmailError] = useState('');
    const [passwordError, setPasswordError] = useState('');
    const [formError, setFormError] = useState('');
    const [localEmail, setLocalEmail] = useState(email);
    const [localPassword, setLocalPassword] = useState(password);
    const [keyboardVisible, setKeyboardVisible] = useState(false);

    // –ê–Ω–∏–º–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è —Å–º–µ—â–µ–Ω–∏—è —Ñ–æ—Ä–º—ã
    const formMarginTop = useRef(new Animated.Value(0)).current;

    // –°–ª—É—à–∞—Ç–µ–ª–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
    useEffect(() => {
        const keyboardWillShowListener = Keyboard.addListener(
            Platform.OS === 'ios' ? 'keyboardWillShow' : 'keyboardDidShow',
            (event) => {
                setKeyboardVisible(true);
                // –ê–Ω–∏–º–∏—Ä—É–µ–º —Å–º–µ—â–µ–Ω–∏–µ —Ñ–æ—Ä–º—ã –≤–≤–µ—Ä—Ö –ø—Ä–∏ –ø–æ—è–≤–ª–µ–Ω–∏–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
                Animated.timing(formMarginTop, {
                    toValue: -normalize(60), // –°–º–µ—â–∞–µ–º —Ñ–æ—Ä–º—É –≤–≤–µ—Ä—Ö
                    duration: Platform.OS === 'ios' ? event.duration : 300,
                    useNativeDriver: false
                }).start();
            }
        );

        const keyboardWillHideListener = Keyboard.addListener(
            Platform.OS === 'ios' ? 'keyboardWillHide' : 'keyboardDidHide',
            (event) => {
                setKeyboardVisible(false);
                // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ñ–æ—Ä–º—É –≤ –∏—Å—Ö–æ–¥–Ω–æ–µ –ø–æ–ª–æ–∂–µ–Ω–∏–µ –ø—Ä–∏ —Å–∫—Ä—ã—Ç–∏–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
                Animated.timing(formMarginTop, {
                    toValue: 0,
                    duration: Platform.OS === 'ios' ? event.duration : 300,
                    useNativeDriver: false
                }).start();
            }
        );

        return () => {
            keyboardWillShowListener.remove();
            keyboardWillHideListener.remove();
        };
    }, []);

    useEffect(() => {
        setLocalEmail(email);
    }, [email]);

    useEffect(() => {
        setLocalPassword(password);
    }, [password]);

    useEffect(() => {
        if (email) setEmailError('');
        if (password) setPasswordError('');
        if (email || password) setFormError('');
    }, [email, password]);

    useEffect(() => {
        if (error && !isLoading) {
            handleErrorDisplay(error);
        } else {
            setEmailError('');
            setPasswordError('');
            setFormError('');
        }
    }, [error, isLoading]);

    const handleErrorDisplay = (errorMessage) => {
        if (!errorMessage) return;

        console.log('Processing error:', errorMessage);

        const lowerCaseError = typeof errorMessage === 'string'
            ? errorMessage.toLowerCase()
            : '';

        if (lowerCaseError.includes('–Ω–µ–≤–µ—Ä–Ω—ã–π email') ||
            lowerCaseError.includes('–Ω–µ –Ω–∞–π–¥–µ–Ω')) {
            setEmailError('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º email –Ω–µ –Ω–∞–π–¥–µ–Ω');
            setPasswordError('');
            setFormError('');
        } else if (lowerCaseError.includes('–ø–∞—Ä–æ–ª—å') ||
            lowerCaseError.includes('password') ||
            lowerCaseError.includes('credentials')) {
            setPasswordError('–ù–µ–≤–µ—Ä–Ω—ã–π –ø–∞—Ä–æ–ª—å');
            setEmailError('');
            setFormError('');
        } else if (lowerCaseError.includes('401') ||
            lowerCaseError.includes('unauthorized') ||
            lowerCaseError.includes('–Ω–µ–≤–µ—Ä–Ω')) {
            setPasswordError('–ù–µ–≤–µ—Ä–Ω—ã–π email –∏–ª–∏ –ø–∞—Ä–æ–ª—å');
            setEmailError('');
            setFormError('');
        } else {
            setFormError(errorMessage);
            setEmailError('');
            setPasswordError('');
        }
    };

    const validateForm = () => {
        let isValid = true;

        if (!localEmail) {
            setEmailError('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ email');
            isValid = false;
        } else if (!/\S+@\S+\.\S+/.test(localEmail)) {
            setEmailError('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π email');
            isValid = false;
        }

        if (!localPassword) {
            setPasswordError('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –ø–∞—Ä–æ–ª—å');
            isValid = false;
        }

        return isValid;
    };

    const handleLogin = () => {
        if (isLoading) {
            return;
        }

        setEmailError('');
        setPasswordError('');
        setFormError('');
        dispatch(clearError());

        dispatch(setEmail(localEmail));
        dispatch(setPassword(localPassword));

        if (!validateForm()) {
            return;
        }

        // –ü–æ–ª–Ω—ã–π —Å–±—Ä–æ—Å —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø–µ—Ä–µ–¥ –≤—Ö–æ–¥–æ–º –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        console.log('üîÑ –í—ã–ø–æ–ª–Ω—è–µ–º RESET_APP_STATE –ø–µ—Ä–µ–¥ –≤—Ö–æ–¥–æ–º');
        dispatch({ type: 'RESET_APP_STATE' });

        console.log('üîê –ù–∞—á–∏–Ω–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å –≤—Ö–æ–¥–∞ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è:', localEmail);
        // –í—ã–ø–æ–ª–Ω—è–µ–º –≤—Ö–æ–¥ —Å –æ–±—Ä–∞–±–æ—Ç–∫–æ–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
        dispatch(login({email: localEmail, password: localPassword}))
            .unwrap()
            .then(result => {
                if (result.requiresTwoFactor) {
                    return;
                }

                if (result.tokens && result.user) {
                    console.log('–í—Ö–æ–¥ –≤—ã–ø–æ–ª–Ω–µ–Ω —É—Å–ø–µ—à–Ω–æ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è ID:', result.user.id);

                    dispatch(setTokens(result.tokens));
                    dispatch(setUser(result.user));

                    // –°—Ä–∞–∑—É –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –ø—Ä–æ—Ñ–∏–ª—å –¥–ª—è –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
                    dispatch(fetchProfile());

                    // –¢–∞–∫–∂–µ –∑–∞–≥—Ä—É–∂–∞–µ–º –ø–æ–ª–Ω—ã–π –ø—Ä–æ—Ñ–∏–ª—å —á–µ—Ä–µ–∑ useAuth
                    dispatch(loadUserProfile());

                    // –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º FCM —Ç–æ–∫–µ–Ω –ø–æ—Å–ª–µ —É—Å–ø–µ—à–Ω–æ–≥–æ –ª–æ–≥–∏–Ω–∞
                    setTimeout(async () => {
                        try {
                            console.log('üî• === –ù–ê–ß–ê–õ–û –†–ï–ì–ò–°–¢–†–ê–¶–ò–ò FCM –¢–û–ö–ï–ù–ê –ü–û–°–õ–ï –õ–û–ì–ò–ù–ê ===');
                            console.log('üîî –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ID:', result.user.id);
                            console.log('üîî –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Ä–æ–ª—å:', result.user.role);
                            
                            // –ò—Å–ø–æ–ª—å–∑—É–µ–º –Ω–æ–≤—ã–π FCM —Å–µ—Ä–≤–∏—Å
                            const FCMTokenService = require('@shared/services/FCMTokenService').default;
                            const buildType = FCMTokenService.getBuildType();
                            console.log('üîî Build type:', buildType);

                            // –¢–û–õ–¨–ö–û FCM –¢–û–ö–ï–ù–´
                            if (FCMTokenService.shouldUseFCM()) {
                                console.log('‚úÖ Build type –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç FCM:', buildType);
                                
                                // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º FCM —Å–µ—Ä–≤–∏—Å
                                const initialized = await FCMTokenService.initializeForUser(result.user);
                                
                                if (initialized) {
                                    console.log('üéâ –£–°–ü–ï–•: FCM —Ç–æ–∫–µ–Ω –æ–±—Ä–∞–±–æ—Ç–∞–Ω!');
                                } else {
                                    console.error('‚ùå –û–®–ò–ë–ö–ê: FCM –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –Ω–µ —É–¥–∞–ª–∞—Å—å!');
                                }
                            } else {
                                console.log('üö´ Build type –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç FCM:', buildType);
                                console.log('‚ÑπÔ∏è –î–ª—è FCM —Ç–æ–∫–µ–Ω–æ–≤ —Å–æ–±–µ—Ä–∏—Ç–µ APK: eas build --platform android --profile preview');
                            }
                            
                            console.log('üî• === –ö–û–ù–ï–¶ –†–ï–ì–ò–°–¢–†–ê–¶–ò–ò FCM –¢–û–ö–ï–ù–ê ===');
                        } catch (error) {
                            console.error('‚ùå –ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê –ø—Ä–∏ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏ FCM —Ç–æ–∫–µ–Ω–∞:', {
                                message: error.message,
                                stack: error.stack,
                                name: error.name
                            });
                        }
                    }, 3000); // –ñ–¥–µ–º 3 —Å–µ–∫—É–Ω–¥—ã –¥–ª—è –ø–æ–ª–Ω–æ–π –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏
                }
            })
            .catch(err => {
                console.log('Login error caught:', err);

                // –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫
                if (typeof err === 'string') {
                    if (err.toLowerCase().includes('–ø–∞—Ä–æ–ª—å')) {
                        setPasswordError(err);
                    } else if (err.toLowerCase().includes('email') || err.toLowerCase().includes('–ø–æ—á—Ç–∞')) {
                        setEmailError(err);
                    } else {
                        setFormError(err);
                    }
                } else {
                    setFormError('–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –≤—Ö–æ–¥–µ');
                }
            });
    };


    const handleEmailChange = (text) => {
        setLocalEmail(text);
        setEmailError('');
        setFormError('');
    };

    const handlePasswordChange = (text) => {
        setLocalPassword(text);
        setPasswordError('');
        setFormError('');
    };

    const handleEmailBlur = () => {
        dispatch(setEmail(localEmail));
    };

    const handlePasswordBlur = () => {
        dispatch(setPassword(localPassword));
    };

    return (
        <Animated.View style={[
            styles.formContainer,
            {marginTop: formMarginTop}
        ]}>
            <View style={styles.inputsContainer}>
                <View style={styles.emailInputContainer}>
                    <Text style={styles.inputLabel}>–í–∞—à–∞ –ø–æ—á—Ç–∞/–Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞</Text>
                    <CustomTextInput
                        style={styles.input}
                        value={localEmail}
                        onChangeText={handleEmailChange}
                        onBlur={handleEmailBlur}
                        keyboardType="email-address"
                        autoCapitalize="none"
                        placeholder="Icbrg@gmail.com"
                        editable={!isLoading}
                    />
                    <View style={[
                        styles.inputUnderline,
                        emailError ? styles.errorUnderline : null
                    ]}/>
                    {emailError ? (
                        <Text style={styles.errorText}>{emailError}</Text>
                    ) : null}
                </View>

                <View style={styles.passwordInputContainer}>
                    <Text style={styles.inputLabel}>–í–∞—à –ø–∞—Ä–æ–ª—å</Text>
                    <CustomTextInput
                        style={styles.input}
                        value={localPassword}
                        onChangeText={handlePasswordChange}
                        onBlur={handlePasswordBlur}
                        secureTextEntry
                        placeholder="********"
                        editable={!isLoading}
                    />
                    <View style={[
                        styles.inputUnderline,
                        passwordError ? styles.errorUnderline : null
                    ]}/>
                    {passwordError ? (
                        <Text style={styles.errorText}>{passwordError}</Text>
                    ) : null}
                </View>
            </View>

            {formError ? (
                <Text style={styles.globalErrorText}>{formError}</Text>
            ) : null}

            <TouchableOpacity
                style={[
                    styles.button,
                    isLoading && styles.buttonDisabled
                ]}
                onPress={handleLogin}
                disabled={isLoading}
            >
                {isLoading ? (
                    <ActivityIndicator color="#fff"/>
                ) : (
                    <Text style={styles.buttonText}>–ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç—å</Text>
                )}
            </TouchableOpacity>
        </Animated.View>
    );
};

